// syntax to declare a varaible
// let variableName : type = value

let username: string = "Hoda";   // text
let age: number = 25;            // int/float
let isAdmin: boolean = true;     // true/false
let nothing: null = null;        // null
let notDefined: undefined = undefined; // undefined
let big: bigint = 123456789n;    // big integer
let unique: symbol = Symbol("id"); // unique identifier


